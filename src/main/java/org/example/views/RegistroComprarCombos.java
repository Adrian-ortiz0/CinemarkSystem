    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String cedula = inputCedula.getText();
        if(cedula.isEmpty()){
            JOptionPane.showMessageDialog(this, "Debe ingresar una cedula para avanzar!");
            return;
        }
        Cliente clienteSeleccionado = null;
        clienteSeleccionado = ClientsController.obtenerClientePorCedula(cedula);
        if(clienteSeleccionado != null){
            JOptionPane.showMessageDialog(this, "Cliente: " + clienteSeleccionado.getNombre() + " Seleccionado");
            VenderCombos vc = new VenderCombos(cedula);
            vc.setVisible(true);
            dispose();
        } else {
            JOptionPane.showMessageDialog(this, "No existe el cliente, se pasara a registrar");
            PreRegistroCombos prr = new PreRegistroCombos();
            prr.setVisible(true);
            dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed
