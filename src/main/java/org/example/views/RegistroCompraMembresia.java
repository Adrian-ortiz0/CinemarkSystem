    private void enterButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enterButtonActionPerformed
        String cedula = inputCedula.getText();
        if(cedula.isEmpty()){
            JOptionPane.showMessageDialog(this, "El campo cedula esta vacio");
            return;
        }
        Cliente cliente = ClientsController.obtenerClientePorCedula(cedula);
        if(cliente == null){
            JOptionPane.showMessageDialog(this, "El cliente no existe, se pasara a registrar");
            PreRegistroMembresia prm = new PreRegistroMembresia();
            prm.setVisible(true);
            dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Cliente " + cliente.getNombre() + " seleccionado!");
            VenderMembresia vm = new VenderMembresia(cliente.getCedula());
            vm.setVisible(true);
            dispose();
        }
    }//GEN-LAST:event_enterButtonActionPerformed

